# Deskripsi: menerina sebuah karakter dan bernilai benar jika karakter
# tersebut adalah huruf 'A'

# Definisi dan spesifikasi dari fungsi isAnA adalah:
# isAnA : character --> boolean
# {isAnA(c) benar jika c adalah karakter (huruf) 'A'}

# Realisasi
def IsAnA(c):
    return (c) == "A"

# Aplikasi
a = IsAnA(1)
print(a)
b = IsAnA(0)
print(b)
c = IsAnA(-1)
print(c)
